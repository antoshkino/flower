<h1>КОНТАКТЫ:</h1>
<p>
В этом цветнике крупная коллекция травянистых пионов организована на газоне в форме концентрических полуокружностей. 
На зиму листья этих цветов полностью отмирают и подлежат обрезке, поэтому растения прекрасно зимуют под снегом. Единственное,
чего они не любят, так это сезонного вымокания и свежей органики. И то и другое может привести к гибели цветка.
           </p> 